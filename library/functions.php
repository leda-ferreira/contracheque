/**
 * Retorna o nome do mês.
 * @param string $mes
 * @return string
 */
function contracheque_nome_mes($mes)
{
    $mes = str_pad($mes, 2, '0', STR_PAD_LEFT);
    $meses = [
        '01' => 'Janeiro',
        '02' => 'Fevereiro',
        '03' => 'Março',
        '04' => 'Abril',
        '05' => 'Maio',
        '06' => 'Junho',
        '07' => 'Julho',
        '08' => 'Agosto',
        '09' => 'Setembro',
        '10' => 'Outubro',
        '11' => 'Novembro',
        '12' => 'Dezembro',
    ];
    return $meses[$mes];
}
